using Raylib_cs;
using Prowl.Wicked.Core;
using Prowl.Wicked.Network;
using Prowl.Wicked.Transport.Telepathy;
using ShooterGame;
using System.Numerics;

// Game settings
const int ScreenWidth = 800;
const int ScreenHeight = 600;
const int DefaultPort = 7777;

// Game state
GameState gameState = GameState.Menu;
string inputText = "";
bool isTypingIp = false;
string statusMessage = "";

// Initialize Raylib
Raylib.InitWindow(ScreenWidth, ScreenHeight, "Prowl.Wicked Shooter - Press H to Host, J to Join");
Raylib.SetTargetFPS(60);

// Initialize networking
var transport = new TelepathyTransport();
NetworkManager.Initialize(transport);

// RPC handlers are automatically generated by the IL weaver
// No manual registration needed when using [Command] and [ClientRpc] attributes

// Subscribe to network events
NetworkManager.OnNetworkStarted += mode =>
{
    statusMessage = $"Started as {mode}";
    gameState = GameState.Playing;

    if (NetworkManager.IsServer)
    {
        // Spawn the local player for host
        SpawnLocalPlayer();
    }
};

NetworkManager.OnNetworkStopped += () =>
{
    statusMessage = "Disconnected";
    gameState = GameState.Menu;
};

NetworkServer.OnClientConnected += conn =>
{
    Console.WriteLine($"Client {conn.ConnectionId} connected");
    // Create entity, add behaviour, THEN spawn (like Unity prefabs)
    var player = NetworkManager.CreateDeferred();
    player.AddBehaviour<PlayerBehaviour>();
    NetworkManager.FinalizeSpawn(player, conn, isLocalPlayer: true);
};

NetworkClient.OnConnected += () =>
{
    statusMessage = "Connected to server!";
};

// Main game loop
while (!Raylib.WindowShouldClose())
{
    float deltaTime = Raylib.GetFrameTime();

    // Update
    switch (gameState)
    {
        case GameState.Menu:
            UpdateMenu();
            break;
        case GameState.Playing:
            UpdateGame(deltaTime);
            break;
    }

    // Draw
    Raylib.BeginDrawing();
    Raylib.ClearBackground(Color.DarkGray);

    switch (gameState)
    {
        case GameState.Menu:
            DrawMenu();
            break;
        case GameState.Playing:
            DrawGame();
            break;
    }

    // Always show status
    if (!string.IsNullOrEmpty(statusMessage))
    {
        Raylib.DrawText(statusMessage, 10, ScreenHeight - 25, 16, Color.Yellow);
    }

    Raylib.EndDrawing();
}

// Cleanup
NetworkManager.Shutdown();
Raylib.CloseWindow();

// ============ Menu Functions ============

void UpdateMenu()
{
    // Handle keyboard input for IP address
    if (isTypingIp)
    {
        int key = Raylib.GetCharPressed();
        while (key > 0)
        {
            if (key >= 32 && key <= 126 && inputText.Length < 20)
            {
                inputText += (char)key;
            }
            key = Raylib.GetCharPressed();
        }

        if (Raylib.IsKeyPressed(KeyboardKey.Backspace) && inputText.Length > 0)
        {
            inputText = inputText[..^1];
        }

        if (Raylib.IsKeyPressed(KeyboardKey.Enter))
        {
            string ip = string.IsNullOrEmpty(inputText) ? "localhost" : inputText;
            statusMessage = $"Connecting to {ip}...";
            NetworkManager.StartClient(ip, DefaultPort);
            isTypingIp = false;
        }

        if (Raylib.IsKeyPressed(KeyboardKey.Escape))
        {
            isTypingIp = false;
            inputText = "";
        }
    }
    else
    {
        if (Raylib.IsKeyPressed(KeyboardKey.H))
        {
            statusMessage = "Starting host...";
            NetworkManager.StartHost(DefaultPort);
        }

        if (Raylib.IsKeyPressed(KeyboardKey.J))
        {
            isTypingIp = true;
            inputText = "";
            statusMessage = "Enter server IP (or press Enter for localhost):";
        }
    }
}

